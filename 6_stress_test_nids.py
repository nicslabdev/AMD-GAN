        ax.set_xticks(x)
        ax.set_xticklabels(models, rotation=15)
        ax.set_ylim(0, 1.05)
        ax.legend()
        for i, (b, a) in enumerate(zip(benign_f1, attack_f1)):
            ax.text(i - width/2, b + 0.02, f'{b:.3f}', ha='center', fontsize=8)
            ax.text(i + width/2, a + 0.02, f'{a:.3f}', ha='center', fontsize=8)

        plt.tight_layout()
        path = os.path.join(output_dir, 'plot2_binary_f1_scenario_d.png')
        plt.savefig(path, bbox_inches='tight')
