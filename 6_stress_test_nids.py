def plot_confusion_matrix_botnet(all_results, label_encoder, output_dir, mode='multiclass'):
    """
    Graph 3: Confusion matrix of the best model under Scenario B (Botnet Storm).
    """
    mode_label = mode.upper()
    print(f"\n  Generating Plot 3: Confusion Matrix (Scenario B - Botnet Storm) [{mode_label}]...")

    results_b = [r for r in all_results if r['Scenario'] == 'Scenario_B']
    if not results_b:
        print("    [SKIP] Scenario B not found.")
        return

    best_b = max(results_b, key=lambda x: x['F1_Macro'])
    print(f"    Best model on Scenario B: {best_b['Model']} (F1-Macro={best_b['F1_Macro']:.4f})")

    cm = best_b['Confusion_Matrix']

    # Determine which classes are actually present in this scenario
    # The CM may have rows/cols for all encoded classes, but only some are present
    class_names = label_encoder.classes_

    fig, axes = plt.subplots(1, 2, figsize=(18, 7))

    # Raw counts
    ax = axes[0]
    sns.heatmap(
        pd.DataFrame(cm, index=class_names, columns=class_names),
        annot=True, fmt='d', cmap='Blues', ax=ax,
        linewidths=0.5, cbar_kws={'label': 'Count'}
    )
    ax.set_title(f'Confusion Matrix (Counts)\n{best_b["Model"]} on Scenario B: Botnet Storm [{mode_label}]',
                 fontweight='bold')
    ax.set_xlabel('Predicted')
    ax.set_ylabel('True')

    # Normalized (recall-based)
    ax = axes[1]
    cm_norm = cm.astype(float)
    row_sums = cm_norm.sum(axis=1, keepdims=True)
    row_sums[row_sums == 0] = 1  # avoid division by zero
    cm_norm = cm_norm / row_sums

    sns.heatmap(
        pd.DataFrame(cm_norm, index=class_names, columns=class_names),
        annot=True, fmt='.2%', cmap='Oranges', ax=ax,
        linewidths=0.5, vmin=0, vmax=1, cbar_kws={'label': 'Rate'}
    )
    ax.set_title(f'Confusion Matrix (Normalized by True Class)\n{best_b["Model"]} on Scenario B: Botnet Storm [{mode_label}]',
                 fontweight='bold')
    ax.set_xlabel('Predicted')
    ax.set_ylabel('True')

    plt.tight_layout()
    path = os.path.join(output_dir, f'plot3_confusion_matrix_botnet_{mode}.png')
    plt.savefig(path, bbox_inches='tight')
    plt.close()
    print(f"    Saved: {path}")

    # Also generate CM for Scenario D (Nightmare) with best overall model
    results_d = [r for r in all_results if r['Scenario'] == 'Scenario_D']
    if results_d:
        best_overall = max(results_d, key=lambda x: x['F1_Macro'])
        cm_d = best_overall['Confusion_Matrix']

        fig, ax = plt.subplots(figsize=(9, 7))
        cm_d_norm = cm_d.astype(float)
        row_sums_d = cm_d_norm.sum(axis=1, keepdims=True)
        row_sums_d[row_sums_d == 0] = 1
        cm_d_norm = cm_d_norm / row_sums_d

        sns.heatmap(
            pd.DataFrame(cm_d_norm, index=class_names, columns=class_names),
            annot=True, fmt='.2%', cmap='Reds', ax=ax,
            linewidths=0.5, vmin=0, vmax=1, cbar_kws={'label': 'Rate'}
        )
        ax.set_title(f'Confusion Matrix (Normalized)\n{best_overall["Model"]} on Scenario D: Nightmare [{mode_label}]',
                     fontweight='bold')
        ax.set_xlabel('Predicted')
        ax.set_ylabel('True')
        plt.tight_layout()
        path_d = os.path.join(output_dir, f'plot3b_confusion_matrix_nightmare_{mode}.png')
        plt.savefig(path_d, bbox_inches='tight')
        plt.close()
        print(f"    Saved: {path_d}")


