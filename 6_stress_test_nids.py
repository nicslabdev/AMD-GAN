def main():
    parser = argparse.ArgumentParser(
        description='NIDS Robustness Audit: Stress-Testing with GAN-Generated Adversarial Traffic',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Full experiment (all phases)
  python stress_test_nids.py

  # Specify test size
  python stress_test_nids.py --test-size 0.3

  # Custom output directory
  python stress_test_nids.py --output results_stress_test/my_run
        """
    )
    parser.add_argument('--test-size', type=float, default=0.3,
                        help='Proportion of real data for test set (default: 0.3)')
    parser.add_argument('--output', type=str, default=None,
                        help='Custom output directory')
    parser.add_argument('--skip-plots', action='store_true',
                        help='Skip plot generation')
    parser.add_argument('--gpu', type=str, default='0',
                        help='GPU to use for GAN inference (default: 0)')

    args = parser.parse_args()

    os.environ["CUDA_VISIBLE_DEVICES"] = args.gpu

    # ── Setup output directory ──
    if args.output:
        output_dir = args.output
    else:
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        output_dir = os.path.join(OUTPUT_DIR, f'stress_test_{timestamp}')
    os.makedirs(output_dir, exist_ok=True)

    print_header()
    print(f"\n  Output directory: {output_dir}")

    # ── Save experiment config ──
    config = {
        'timestamp': datetime.now().isoformat(),
        'test_size': args.test_size,
        'models_dir': MODELS_DIR,
        'random_state': RANDOM_STATE,
        'models': list(VICTIM_MODELS.keys()),
        'scenarios': {k: v['distribution'] for k, v in STRESS_SCENARIOS.items()},
        'modes': ['multiclass', 'binary'],
    }
    with open(os.path.join(output_dir, 'experiment_config.json'), 'w') as f:
        json.dump(config, f, indent=2)

    start_total = datetime.now()

    # =========================================================================
    # PHASE 0: Load and prepare real data
    # =========================================================================
    df_raw = load_real_data()
    df_features = prepare_features(df_raw, target_classes=ALL_CLASSES)

    print(f"\n  Filtered dataset: {len(df_features):,} samples")
    print(f"  Classes (multiclass): {ALL_CLASSES}")
    print(f"  Classes (binary):     {BINARY_CLASSES}")
    print(f"\n  Multiclass Distribution:")
    for cls in ALL_CLASSES:
        cnt = len(df_features[df_features['Label_Class'] == cls])
        pct = (cnt / len(df_features)) * 100
        print(f"    {cls:<15}: {cnt:>10,} ({pct:>5.1f}%)")

    # Binary distribution
    benign_cnt = len(df_features[df_features['Label_Class'] == 'BENIGN'])
    attack_cnt = len(df_features) - benign_cnt
    print(f"\n  Binary Distribution:")
    print(f"    {'BENIGN':<15}: {benign_cnt:>10,} ({benign_cnt/len(df_features)*100:>5.1f}%)")
    print(f"    {'ATTACK':<15}: {attack_cnt:>10,} ({attack_cnt/len(df_features)*100:>5.1f}%)")

    feature_cols = [c for c in df_features.columns if c != 'Label_Class']
    X = df_features[feature_cols].values

    # Multiclass label encoder
    label_encoder_multi = LabelEncoder()
    label_encoder_multi.fit(ALL_CLASSES)
    y_multi = label_encoder_multi.transform(df_features['Label_Class'])

    # Binary label encoder
    label_encoder_binary = LabelEncoder()
    label_encoder_binary.fit(BINARY_CLASSES)
    y_binary = np.where(df_features['Label_Class'].values == 'BENIGN', 0, 1)

    print(f"\n  Features: {len(feature_cols)}")
    print(f"  Multiclass encoded classes: {list(label_encoder_multi.classes_)}")
    print(f"  Binary encoded classes:     {list(label_encoder_binary.classes_)}")

    # Train/test split (same split for both modes to ensure fair comparison)
    X_train, X_test, y_train_multi, y_test_multi, y_train_binary, y_test_binary = \
        train_test_split(
            X, y_multi, y_binary,
            test_size=args.test_size,
            random_state=RANDOM_STATE,
            stratify=y_multi
        )
    print(f"\n  Train: {len(X_train):,} | Test: {len(X_test):,}")

    # Fit global scaler on training data (shared across both modes)
    scaler = MinMaxScaler()
    scaler.fit(X_train)
    X_train_clean = np.nan_to_num(X_train, nan=0, posinf=0, neginf=0)

    # =========================================================================
    # PHASE 1: Train Victim NIDS (both modes)
    # =========================================================================
    trained_models_multi = train_victim_models(X_train_clean, y_train_multi, scaler, mode='multiclass')
    trained_models_binary = train_victim_models(X_train_clean, y_train_binary, scaler, mode='binary')

    # =========================================================================
    # PHASE 2: Generate Stress Scenarios (shared — labels converted at eval time)
    # =========================================================================
    scenarios = generate_stress_scenarios()

    # =========================================================================
    # PHASE 3: Massive Evaluation — MULTICLASS
    # =========================================================================
    results_multi = run_massive_evaluation(
        trained_models_multi, scaler, label_encoder_multi,
        X_test, y_test_multi,
        scenarios, output_dir, mode='multiclass'
    )

    # =========================================================================
    # PHASE 3: Massive Evaluation — BINARY
    # =========================================================================
    results_binary = run_massive_evaluation(
        trained_models_binary, scaler, label_encoder_binary,
        X_test, y_test_binary,
        scenarios, output_dir, mode='binary'
    )

    # Save numerical results BEFORE plotting
    results_df_multi = save_results_csv(results_multi, output_dir, mode='multiclass')
    results_df_binary = save_results_csv(results_binary, output_dir, mode='binary')

    # Also save a combined CSV
    combined_df = pd.concat([results_df_multi, results_df_binary], ignore_index=True)
    combined_csv_path = os.path.join(output_dir, 'stress_test_results_combined.csv')
    combined_df.to_csv(combined_csv_path, index=False)
    print(f"\n  [SAVED] Combined Results CSV: {combined_csv_path}")

    # =========================================================================
    # PHASE 4: Visualizations (both modes)
    # =========================================================================
    if not args.skip_plots:
        generate_all_plots(results_multi, label_encoder_multi, output_dir, mode='multiclass')
        generate_all_plots(results_binary, label_encoder_binary, output_dir, mode='binary')

        # ── Comparative plot: Multiclass vs Binary ──
        print("\n  Generating Comparative Plot: Multiclass vs Binary...")
        set_paper_style()
        _plot_multiclass_vs_binary(results_multi, results_binary, output_dir)

    # =========================================================================
    # Summary (both modes)
    # =========================================================================
    generate_text_summary(results_multi, label_encoder_multi, output_dir, mode='multiclass')
    generate_text_summary(results_binary, label_encoder_binary, output_dir, mode='binary')

    elapsed_total = datetime.now() - start_total
    print(f"\n{'=' * 100}")
    print(f"  EXPERIMENT COMPLETED IN {elapsed_total}")
    print(f"  Results: {output_dir}")
    print(f"  CSV (multiclass): {os.path.join(output_dir, 'stress_test_results_multiclass.csv')}")
    print(f"  CSV (binary):     {os.path.join(output_dir, 'stress_test_results_binary.csv')}")
    print(f"  CSV (combined):   {combined_csv_path}")
    print(f"  Plots:            {os.path.join(output_dir, 'plots/')}")
    print(f"  Summary (multi):  {os.path.join(output_dir, 'STRESS_TEST_SUMMARY_multiclass.txt')}")
    print(f"  Summary (binary): {os.path.join(output_dir, 'STRESS_TEST_SUMMARY_binary.txt')}")
    print(f"{'=' * 100}")


if __name__ == "__main__":
    main()
