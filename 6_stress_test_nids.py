def evaluate_model_on_scenario(model, model_name, X_test, y_test,
                                label_encoder, scenario_name, mode='multiclass'):
    """Evaluate a single model on a single scenario."""
    y_pred = model.predict(X_test)

    # Use all encoded class indices so metrics are consistent even when
    # a scenario only contains a subset of classes.
    all_labels = list(range(len(label_encoder.classes_)))

    accuracy = accuracy_score(y_test, y_pred)
    f1_macro = f1_score(y_test, y_pred, average='macro', zero_division=0, labels=all_labels)
    f1_weighted = f1_score(y_test, y_pred, average='weighted', zero_division=0, labels=all_labels)
    precision_macro = precision_score(y_test, y_pred, average='macro', zero_division=0, labels=all_labels)
    recall_macro = recall_score(y_test, y_pred, average='macro', zero_division=0, labels=all_labels)

    report = classification_report(
        y_test, y_pred,
        labels=all_labels,
        target_names=label_encoder.classes_,
        output_dict=True,
        zero_division=0
    )

    cm = confusion_matrix(y_test, y_pred, labels=all_labels)

    return {
        'Model': model_name,
        'Scenario': scenario_name,
        'Mode': mode,
        'Accuracy': accuracy,
        'F1_Macro': f1_macro,
        'F1_Weighted': f1_weighted,
        'Precision_Macro': precision_macro,
        'Recall_Macro': recall_macro,
        'Test_Samples': len(y_test),
        'Report': report,
        'Confusion_Matrix': cm,
    }


def run_massive_evaluation(trained_models, scaler, label_encoder,
                            X_test_real, y_test_real,
                            scenarios, output_dir, mode='multiclass'):
    """Evaluate every model on every scenario (including real baseline)."""
    mode_label = mode.upper()
    print("\n" + "=" * 70)
    print(f"[PHASE 3] MASSIVE EVALUATION - {mode_label}")
    print("=" * 70)

    all_results = []

    # --- Real baseline ---
    print(f"\n  {'━' * 60}")
    print(f"  [{mode_label}] Evaluating on REAL TEST SET (baseline)")
    print(f"  {'━' * 60}")

    X_real_scaled = scaler.transform(X_test_real)
    X_real_scaled = np.nan_to_num(X_real_scaled, nan=0, posinf=0, neginf=0)

    for model_name, model in trained_models.items():
        result = evaluate_model_on_scenario(
            model, model_name, X_real_scaled, y_test_real,
            label_encoder, 'Real_Test', mode=mode
        )
        all_results.append(result)
        print(f"    {model_name:<15} Acc={result['Accuracy']:.4f}  "
              f"F1-M={result['F1_Macro']:.4f}  F1-W={result['F1_Weighted']:.4f}")

    # --- Synthetic stress scenarios ---
    for scenario_id, df_scenario in scenarios.items():
        scenario_name = STRESS_SCENARIOS[scenario_id]['name']
        print(f"\n  {'━' * 60}")
        print(f"  [{mode_label}] Evaluating on {scenario_id}: {scenario_name}")
        print(f"  Samples: {len(df_scenario):,}")
        print(f"  {'━' * 60}")

        # For binary mode, convert labels
        if mode == 'binary':
            df_eval = convert_scenario_to_binary(df_scenario)
        else:
            df_eval = df_scenario.copy()

        # Align features: use only FEATURE_NAMES columns present in df_eval
        feature_cols = [c for c in FEATURE_NAMES if c in df_eval.columns]
        X_scenario = df_eval[feature_cols].values
        X_scenario = np.nan_to_num(X_scenario, nan=0, posinf=0, neginf=0)

        # Encode labels — handle classes not seen during training
        scenario_classes = df_eval['Label_Class'].unique()
        known_classes = set(label_encoder.classes_)
        unknown = set(scenario_classes) - known_classes
        if unknown:
            print(f"    [WARN] Unknown classes in scenario: {unknown}. Skipping those samples.")
            df_eval = df_eval[df_eval['Label_Class'].isin(known_classes)].copy()
            X_scenario = df_eval[feature_cols].values
            X_scenario = np.nan_to_num(X_scenario, nan=0, posinf=0, neginf=0)

        y_scenario = label_encoder.transform(df_eval['Label_Class'])

        X_scenario_scaled = scaler.transform(X_scenario)
        X_scenario_scaled = np.nan_to_num(X_scenario_scaled, nan=0, posinf=0, neginf=0)

        for model_name, model in trained_models.items():
            result = evaluate_model_on_scenario(
                model, model_name, X_scenario_scaled, y_scenario,
                label_encoder, scenario_id, mode=mode
            )
            all_results.append(result)
            print(f"    {model_name:<15} Acc={result['Accuracy']:.4f}  "
                  f"F1-M={result['F1_Macro']:.4f}  F1-W={result['F1_Weighted']:.4f}")

    return all_results


