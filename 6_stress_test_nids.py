    mode_label = mode.upper()
    print(f"\n  Generating Plot 2: Radar Chart (Scenario D - Nightmare) [{mode_label}]...")

    if mode == 'binary':
        # For binary, generate a grouped bar chart instead of radar
        results_d = [r for r in all_results if r['Scenario'] == 'Scenario_D']
        if not results_d:
            print("    [SKIP] Scenario D not found.")
            return

        fig, ax = plt.subplots(figsize=(10, 6))
