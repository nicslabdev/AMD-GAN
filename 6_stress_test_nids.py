def get_available_generators():
    """Discover available WGAN-GP v2 generators."""
    available = {}
    for class_name, folder in CLASS_TO_FOLDER.items():
        model_path = os.path.join(MODELS_DIR, folder, f'generator_{folder}.h5')
        if os.path.exists(model_path):
            available[class_name] = {
                'folder': folder,
                'model_path': model_path,
                'source_dir': MODELS_DIR,
            }
    return available


def load_scaler_for_class(class_name, source_dir):
    """Load or recreate the MinMaxScaler for a given class."""
    folder = CLASS_TO_FOLDER[class_name]

    search_dirs = [source_dir] if source_dir else [MODELS_DIR]

    for sd in search_dirs:
        if sd is None:
            continue
        scaler_path = os.path.join(sd, folder, 'scaler.pkl')
        if os.path.exists(scaler_path):
            with open(scaler_path, 'rb') as f:
                return pickle.load(f)

    # Fallback: recreate scaler from original dataset
    print(f"    Recreating scaler for {class_name} from original data...")

    df_pl = pl.read_csv(REAL_DATA_PATH, low_memory=False)
    df = df_pl.to_pandas()
    df.columns = df.columns.str.strip()
    df_cls = df[df['Attack Type'] == class_name].copy()

    features = df_cls[FEATURES_BASE].copy()

    octets = df_cls['Source IP'].astype(str).str.split('.', expand=True)
    for i in range(4):
        features[f'Src_IP_{i+1}'] = pd.to_numeric(octets[i], errors='coerce').fillna(0).astype(int)

    octets = df_cls['Destination IP'].astype(str).str.split('.', expand=True)
    for i in range(4):
        features[f'Dst_IP_{i+1}'] = pd.to_numeric(octets[i], errors='coerce').fillna(0).astype(int)

    for col in LOG_COLUMNS:
        features[col] = np.log1p(features[col].clip(lower=0))
        features[col] = features[col].clip(lower=-20, upper=20)

    features.replace([np.inf, -np.inf], np.nan, inplace=True)
    features.fillna(0, inplace=True)

    scaler = MinMaxScaler(feature_range=(-1, 1))
    scaler.fit(features[FEATURE_NAMES])

    scaler_path = os.path.join(source_dir or MODELS_DIR, folder, 'scaler.pkl')
    try:
        with open(scaler_path, 'wb') as f:
            pickle.dump(scaler, f)
    except Exception:
        pass

    return scaler


def generate_gan_samples(generator, n_samples, latent_dim=LATENT_DIM, batch_size=1024):
    """Generate synthetic samples from a WGAN-GP generator."""
    all_samples = []
    remaining = n_samples

    while remaining > 0:
        current_batch = min(batch_size, remaining)
        noise = np.random.normal(0, 1, (current_batch, latent_dim))
        samples = generator.predict(noise, verbose=0)
        all_samples.append(samples)
        remaining -= current_batch

    return np.vstack(all_samples)


def reconstruct_features(X_synthetic, scaler):
    """Inverse-transform GAN output back to original feature space."""
    X_inv = scaler.inverse_transform(X_synthetic)
    df_rec = pd.DataFrame(X_inv, columns=FEATURE_NAMES)

    for col in LOG_COLUMNS:
        df_rec[col] = np.expm1(df_rec[col])

    for col in df_rec.columns:
        if col.startswith('Src_IP_') or col.startswith('Dst_IP_'):
            df_rec[col] = df_rec[col].round().clip(0, 255).astype(int)

    df_rec['Source Port'] = df_rec['Source Port'].round().clip(1, 65535).astype(int)
    df_rec['Destination Port'] = df_rec['Destination Port'].round().clip(1, 65535).astype(int)
    df_rec['Protocol'] = df_rec['Protocol'].round().clip(1, 255).astype(int)

    for col in LOG_COLUMNS + ['SYN Flag Count', 'ACK Flag Count',
                               'FIN Flag Count', 'RST Flag Count', 'PSH Flag Count']:
        df_rec[col] = df_rec[col].clip(lower=0)

    integer_columns = ['Total Fwd Packets', 'Total Backward Packets',
                        'SYN Flag Count', 'ACK Flag Count',
                        'FIN Flag Count', 'RST Flag Count', 'PSH Flag Count']
    for col in integer_columns:
        df_rec[col] = df_rec[col].round().astype(int)

    return df_rec


def generate_stress_scenarios():
    """Generate all stress-test datasets using WGAN-GP v2 models."""
    print("\n" + "=" * 70)
    print("[PHASE 2] GENERATING STRESS-TEST SCENARIOS VIA GAN")
    print("=" * 70)

    available = get_available_generators()
    print(f"\n  Available generators: {len(available)}")
    for cls, info in available.items():
        print(f"    {cls:<15} -> {info['model_path']}")

    scenarios = {}

    for scenario_id, scenario_cfg in STRESS_SCENARIOS.items():
        print(f"\n  {'─' * 60}")
        print(f"  Generating {scenario_id}: {scenario_cfg['name']}")
        print(f"  {scenario_cfg['description']}")
        print(f"  {'─' * 60}")

        distribution = scenario_cfg['distribution']
        parts = []

        for class_name, n_samples in distribution.items():
            if n_samples <= 0:
                continue

            if class_name not in available:
                print(f"    [WARN] No generator for '{class_name}', skipping.")
                continue

            print(f"    {class_name}: generating {n_samples:,} samples...", end=" ", flush=True)

            gen_info = available[class_name]
            generator = load_model(gen_info['model_path'], compile=False)
            gan_scaler = load_scaler_for_class(class_name, gen_info['source_dir'])

            X_synth = generate_gan_samples(generator, n_samples)
            df_synth = reconstruct_features(X_synth, gan_scaler)
            df_synth['Label_Class'] = class_name

            parts.append(df_synth)
            print(f"OK ({len(df_synth):,})")

            del generator
            tf.keras.backend.clear_session()

        if parts:
            df_scenario = pd.concat(parts, ignore_index=True)
            df_scenario = df_scenario.sample(frac=1, random_state=RANDOM_STATE).reset_index(drop=True)
            scenarios[scenario_id] = df_scenario

            total = len(df_scenario)
            print(f"\n    Total {scenario_id}: {total:,} samples")
            for cls in distribution:
                if cls in df_scenario['Label_Class'].values:
                    cnt = len(df_scenario[df_scenario['Label_Class'] == cls])
                    print(f"      {cls:<15}: {cnt:>10,} ({cnt/total*100:>5.1f}%)")

    print(f"\n  [OK] {len(scenarios)} scenarios generated.")
    return scenarios


