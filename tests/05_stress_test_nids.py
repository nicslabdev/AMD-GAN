def prepare_features (df :pd .DataFrame ,target_classes =None )->pd .DataFrame :
    """Prepare feature matrix from raw CIC-IDS2017 data."""
    df =df .copy ()

    label_col =LABEL_COL 
    df ['Label_Class']=df [label_col ]

    if target_classes is not None :
        df =df [df ['Label_Class'].isin (target_classes )].copy ()

    features_df =df [FEATURES_BASE ].copy ()


    if IP_COLS [0 ]in df .columns :
        octets =df [IP_COLS [0 ]].astype (str ).str .split ('.',expand =True )
        for i in range (4 ):
            features_df [f'Src_IP_{i +1 }']=pd .to_numeric (octets .get (i ,0 ),errors ='coerce').fillna (0 ).astype (int )
    else :
        for i in range (4 ):
            features_df [f'Src_IP_{i +1 }']=0 

    if IP_COLS [1 ]in df .columns :
        octets =df [IP_COLS [1 ]].astype (str ).str .split ('.',expand =True )
        for i in range (4 ):
            features_df [f'Dst_IP_{i +1 }']=pd .to_numeric (octets .get (i ,0 ),errors ='coerce').fillna (0 ).astype (int )
    else :
        for i in range (4 ):
            features_df [f'Dst_IP_{i +1 }']=0 


    features_df ['Label_Class']=df ['Label_Class'].values 

    features_df .replace ([np .inf ,-np .inf ],np .nan ,inplace =True )
    features_df .fillna (0 ,inplace =True )

    return features_df 






