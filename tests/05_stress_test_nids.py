def build_scenario_labels (multiline =False ,include_name =False ):
    """Build scenario labels dynamically from current dataset configuration."""
    import textwrap 
    labels ={'Real_Test':'Real Test'}
    for scenario_id in ['Scenario_A','Scenario_B','Scenario_C','Scenario_D']:
        if scenario_id not in STRESS_SCENARIOS :
            continue 
        scenario_name =STRESS_SCENARIOS [scenario_id ]['name']
        scenario_suffix =scenario_id .split ('_')[-1 ]

        base_label =f"Sc. {scenario_suffix }"if multiline else f"{scenario_suffix }"

        if include_name :
            wrapped_name =textwrap .fill (scenario_name ,width =15 )
            labels [scenario_id ]=f"{base_label }\n{wrapped_name }"if multiline else f"{base_label } - {scenario_name }"
        else :
            labels [scenario_id ]=base_label 

    return labels 


