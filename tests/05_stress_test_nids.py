def save_results_csv (all_results ,output_dir ,mode ='multiclass'):
    """Save all numerical results to CSV for reproducibility."""
    mode_label =mode .upper ()
    class_list =ALL_CLASSES if mode =='multiclass'else BINARY_CLASSES 

    rows =[]
    for r in all_results :
        row ={
        'Mode':mode ,
        'Model':r ['Model'],
        'Scenario':r ['Scenario'],
        'Scenario_Name':STRESS_SCENARIOS .get (r ['Scenario'],{}).get ('name','Real Test'),
        'Test_Samples':r ['Test_Samples'],
        'Accuracy':r ['Accuracy'],
        'F1_Macro':r ['F1_Macro'],
        'F1_Weighted':r ['F1_Weighted'],
        'Precision_Macro':r ['Precision_Macro'],
        'Recall_Macro':r ['Recall_Macro'],
        }


        for cls_name in class_list :
            if cls_name in r ['Report']:
                row [f'F1_{cls_name }']=r ['Report'][cls_name ]['f1-score']
                row [f'Precision_{cls_name }']=r ['Report'][cls_name ]['precision']
                row [f'Recall_{cls_name }']=r ['Report'][cls_name ]['recall']

        rows .append (row )

    df =pd .DataFrame (rows )
    csv_path =os .path .join (output_dir ,f'stress_test_results_{mode }.csv')
    df .to_csv (csv_path ,index =False )
    print (f"\n  [SAVED] {mode_label } Results CSV: {csv_path }")


    for r in all_results :
        report_df =pd .DataFrame (r ['Report']).transpose ()
        fname =f"{r ['Model']}_{r ['Scenario']}_{mode }_classification_report.csv"
        report_df .to_csv (os .path .join (output_dir ,fname ))


    for r in all_results :
        cm_labels =class_list if r ['Confusion_Matrix'].shape [0 ]==len (class_list )else [
        f'Class_{i }'for i in range (r ['Confusion_Matrix'].shape [0 ])
        ]
        cm_df =pd .DataFrame (
        r ['Confusion_Matrix'],
        index =cm_labels ,
        columns =cm_labels 
        )
        fname =f"{r ['Model']}_{r ['Scenario']}_{mode }_confusion_matrix.csv"
        cm_df .to_csv (os .path .join (output_dir ,fname ))

    return df 


def generate_text_summary (all_results ,label_encoder ,output_dir ,mode ='multiclass'):
    """Generate a comprehensive text summary of all results for a given mode."""
    mode_label =mode .upper ()
    lines =[]
    lines .append ("="*120 )
    lines .append (" "*20 +f"NIDS ROBUSTNESS AUDIT - STRESS TESTING RESULTS [{mode_label }]")
    lines .append (" "*20 +"Train on Real Data → Test on GAN-Generated Adversarial Traffic")
    lines .append (f" "*40 +f"Date: {datetime .now ().strftime ('%Y-%m-%d %H:%M:%S')}")
    lines .append ("="*120 )


    lines .append ("\n"+"="*120 )
    lines .append ("1. EXPERIMENT OVERVIEW")
    lines .append ("="*120 )
    lines .append (f"\n  Models evaluated: {', '.join (VICTIM_MODELS .keys ())}")
    lines .append (f"  Scenarios: Real Test + {len (STRESS_SCENARIOS )} adversarial scenarios")
    for sid ,scfg in STRESS_SCENARIOS .items ():
        total_s =sum (scfg ['distribution'].values ())
        lines .append (f"    {sid } ({scfg ['name']}): {total_s :,} samples")
        if mode =='multiclass':
            for cls ,n in scfg ['distribution'].items ():
                lines .append (f"      {cls :<15}: {n :>10,} ({n /total_s *100 :>5.1f}%)")
        else :
            benign_n =scfg ['distribution'].get (BENIGN_LABEL ,0 )
            attack_n =total_s -benign_n 
            lines .append (f"      {BENIGN_LABEL :<15}: {benign_n :>10,} ({benign_n /total_s *100 :>5.1f}%)")
            lines .append (f"      {'ATTACK':<15}: {attack_n :>10,} ({attack_n /total_s *100 :>5.1f}%)")


    lines .append ("\n\n"+"="*120 )
    lines .append (f"2. MODEL RANKINGS PER SCENARIO [{mode_label }] (by F1-Macro)")
    lines .append ("="*120 )

    scenarios_seen =sorted (set (r ['Scenario']for r in all_results ),
    key =lambda x :['Real_Test','Scenario_A','Scenario_B',
    'Scenario_C','Scenario_D'].index (x )
    if x in ['Real_Test','Scenario_A','Scenario_B',
    'Scenario_C','Scenario_D']else 99 )

    for scenario in scenarios_seen :
        scfg =STRESS_SCENARIOS .get (scenario ,{'name':'Real Test'})
        s_name =scfg .get ('name','Real Test')
        results_s =sorted ([r for r in all_results if r ['Scenario']==scenario ],
        key =lambda x :x ['F1_Macro'],reverse =True )

        lines .append (f"\n  {'─'*100 }")
        lines .append (f"  {scenario } ({s_name })")
        lines .append (f"  {'─'*100 }")
        lines .append (f"  {'Pos':<5} {'Model':<15} │ {'Accuracy':>10} {'F1-Macro':>10} "
        f"{'F1-Weight':>10} │ {'Precision':>10} {'Recall':>10}")
        lines .append (f"  {'─'*80 }")

        for i ,r in enumerate (results_s ,1 ):
            lines .append (f"  {i :<5} {r ['Model']:<15} │ {r ['Accuracy']:>10.4f} {r ['F1_Macro']:>10.4f} "
            f"{r ['F1_Weighted']:>10.4f} │ {r ['Precision_Macro']:>10.4f} {r ['Recall_Macro']:>10.4f}")


    lines .append ("\n\n"+"="*120 )
    lines .append (f"3. ROBUSTNESS DEGRADATION ANALYSIS [{mode_label }] (Δ vs Real Test)")
    lines .append ("="*120 )

    real_results ={r ['Model']:r for r in all_results if r ['Scenario']=='Real_Test'}

    lines .append (f"\n  {'Model':<15} │ {'Scenario':<25} │ {'Δ Accuracy':>12} {'Δ F1-Macro':>12} "
    f"{'Δ Precision':>12} {'Δ Recall':>12}")
    lines .append (f"  {'─'*100 }")

    for r in all_results :
        if r ['Scenario']=='Real_Test':
            continue 
        baseline =real_results .get (r ['Model'])
        if baseline :
            d_acc =r ['Accuracy']-baseline ['Accuracy']
            d_f1 =r ['F1_Macro']-baseline ['F1_Macro']
            d_prec =r ['Precision_Macro']-baseline ['Precision_Macro']
            d_rec =r ['Recall_Macro']-baseline ['Recall_Macro']

            s_name =STRESS_SCENARIOS .get (r ['Scenario'],{}).get ('name',r ['Scenario'])
            lines .append (f"  {r ['Model']:<15} │ {s_name :<25} │ {d_acc :>+12.4f} {d_f1 :>+12.4f} "
            f"{d_prec :>+12.4f} {d_rec :>+12.4f}")


    lines .append ("\n\n"+"="*120 )
    lines .append (f"4. KEY FINDINGS [{mode_label }]")
    lines .append ("="*120 )


    model_degradation ={}
    for model_name in VICTIM_MODELS :
        baseline =real_results .get (model_name )
        if not baseline :
            continue 
        deltas =[]
        for r in all_results :
            if r ['Model']==model_name and r ['Scenario']!='Real_Test':
                deltas .append (abs (r ['F1_Macro']-baseline ['F1_Macro']))
        if deltas :
            model_degradation [model_name ]=np .mean (deltas )

    if model_degradation :
        most_robust =min (model_degradation ,key =model_degradation .get )
        least_robust =max (model_degradation ,key =model_degradation .get )
        lines .append (f"\n  Most robust model:  {most_robust } "
        f"(avg Δ F1-Macro = {model_degradation [most_robust ]:+.4f})")
        lines .append (f"  Least robust model: {least_robust } "
        f"(avg Δ F1-Macro = {model_degradation [least_robust ]:+.4f})")


    lines .append (f"\n  Worst scenario per model:")
    for model_name in VICTIM_MODELS :
        model_results =[r for r in all_results 
        if r ['Model']==model_name and r ['Scenario']!='Real_Test']
        if model_results :
            worst =min (model_results ,key =lambda x :x ['F1_Macro'])
            s_name =STRESS_SCENARIOS .get (worst ['Scenario'],{}).get ('name',worst ['Scenario'])
            lines .append (f"    {model_name :<15}: {s_name :<25} F1-Macro = {worst ['F1_Macro']:.4f}")

    lines .append ("\n"+"="*120 )

    summary_text ="\n".join (lines )

    path =os .path .join (output_dir ,f'STRESS_TEST_SUMMARY_{mode }.txt')
    with open (path ,'w')as f :
        f .write (summary_text )

    print (f"\n{summary_text }")
    print (f"\n  [SAVED] {mode_label } Summary: {path }")






